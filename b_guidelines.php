<b> Rules in basketball</b>

1. Team Composition:
   - Each team consists of five players on the court: two forwards, two guards, and a center.
   
2. Game Duration:
   - Professional games typically consist of four 12-minute quarters (NBA) or four 10-minute quarters (FIBA). College games have two 20-minute halves.

3. Scoring:
   - Field goals within the three-point line are worth two points.
   - Field goals beyond the three-point line are worth three points.
   - Free throws are worth one point each.

4. Starting Play:
   - The game starts with a jump ball at center court.

5. Dribbling:
   - Players must dribble the ball while moving. Dribbling involves bouncing the ball on the floor repeatedly with one hand.

6. Passing:
   - Players can pass the ball to teammates using various types of passes (chest pass, bounce pass, overhead pass).

7. Shooting:
   - Players aim to shoot the ball through the opponentâ€™s hoop. Shots can be taken from various positions on the court.

8. Defense:
   - Defensive strategies include man-to-man defense (each player guards a specific opponent) and zone defense (players guard an area of the court).

### Violations:
1. Traveling:
   - Taking more than two steps without dribbling the ball.
   
2. Double Dribble:
   - Dribbling the ball with both hands simultaneously or stopping and then restarting dribbling.

3. Carrying:
   - Palming the ball or turning the wrist over while dribbling.

4. Goaltending:
   - Interfering with the ball on its way to the basket or when it is on the rim.

5. Shot Clock Violation:
   - Failing to attempt a shot within the allotted time (24 seconds in the NBA, 14 or 24 seconds after an offensive rebound, 30 seconds in college).

### Fouls:
1. Personal Foul:
   - Physical contact against an opponent. Accumulating a certain number of personal fouls can result in a player fouling out (5 fouls in college, 6 in the NBA).

2. Technical Foul:
   - Unsportsmanlike conduct by a player or coach. Results in free throws for the opposing team.

3. Flagrant Foul:
   - Excessive or violent contact. Results in free throws and possession for the opposing team.

### Game Structure:
1. Substitutions:
   - Players can be substituted in and out of the game during stoppages.

2. Timeouts:
   - Coaches can call timeouts to discuss strategy. The number and length of timeouts vary by league.

3. Overtime:
   - If the game is tied at the end of regulation, overtime periods are played to determine the winner.

