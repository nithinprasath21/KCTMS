<b>Rules in Football?</b>

Here at NAIS Pudong, we follow the general rules and regulations for football with some slight variations on certain rules, which can be found below. Some of the basic rules of football that we adhere to include:

No Hands

Except for the goalie and during throw-ins, players are not permitted to use their hands in play. Hands refer to anything from the fingers to the shoulders.

Throw-ins

When the ball crosses the sidelines and goes out of bounds, a throw-in is taken by a member of the team that did not kick the ball out of bounds. The player taking the throw-in plants both feet on the
ground and use both hands to throw the ball straight overhead and back into play.

Goal and Corner Kicks

Goal and corner kicks are taken when the ball passes out of bounds past the endline. If the offensive team kicks it out, the defensive team takes a goal kick from inside of the goal box. If the defensive team kicks 
it out, the offensive team takes a corner kick from the corner nearest to where the ball went out of bounds. More on how we take goal and corner kicks can be found below.

Game in Play

As long as the ball is on the field of play and in bounds, the game is considered in play. The only exception is if the referee calls for an infraction.

Fouls

In general, fouls occur when a player kicks or strikes another player without the intention of going for the ball. Kicking, tripping, jumping at, striking, pushing, and holding an opponent can all be considered fouls. 
