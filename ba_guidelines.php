<b>Rules in badminton</b>

Equipment:
   Players must use a racket and shuttlecock.
Court: 
   The game is played on a rectangular court divided by a net.
Scoring: 
   Points are scored by landing the shuttlecock in the opponent's court. Games are typically played to 21 points, with scoring only on your serve.
Service: 
   The serve must be below the waist, and the shuttlecock must be struck below the waist. In doubles, the serve must be diagonal to the opponent's court.
Rally: 
   After the serve, players hit the shuttlecock back and forth over the net. It must pass over the net and land in the opponent's court to score a point.
Faults: 
   Mistakes like serving or receiving incorrectly, hitting out of bounds, or touching the net result in faults.
Let:
   A let is called when a rally is interrupted due to unforeseen circumstances, such as a shuttlecock getting stuck in the net.
Footwork and Technique: 
   Players must move quickly and use a variety of shots, including overhead smashes, clears, drops, and net shots.
Sportsmanship: 
   Players should demonstrate good sportsmanship by respecting opponents and officials and playing fairly.
Practice: 
   Improvement comes with regular training sessions focusing on footwork, racket skills, and strategy.
